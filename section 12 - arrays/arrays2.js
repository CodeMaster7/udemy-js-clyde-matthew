// create a shopping list
let shoppingList = ['milk', 'bread', 'beer']
shoppingList;

console.log(shoppingList[0]);
console.log(shoppingList[1]);

// lets override values
shoppingList[0] = 'Coke'
shoppingList;

// accesss the length property
console.log(shoppingList.length);

// add an item to the end of our shopping list
shoppingList.push('toilet paper')
shoppingList;

// remove last item from the array
shoppingList.pop()
shoppingList;